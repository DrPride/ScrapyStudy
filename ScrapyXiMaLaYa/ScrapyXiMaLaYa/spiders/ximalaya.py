    def parse(self, respose):
        soup = BeautifulSoup(respose.body, 'lxml')
        for content in soup.find_all(class_="albumfaceOutter"):
            item = ScrapyximalayaItem()
            item['url'] = content.a['href']
            item['title'] = content.img['alt']
            item['img_url'] = content.img['src']
            # yield item
            yield Request(item['url'], headers=self.headers2, callback=self.get_m4a)

    def another(self, respose):
        item = ScrapyximalayaItem()
        ifanother = respose.xpath('//div[@class="pagingBar_wrapper"]/a[last()-1]/@data-page')
        if len(ifanother):
            num = ifanother[0]
            for n in range(1, int(num)):
                url = item['url'] + '?page={}'.format(n)
                yield Request(url, headers=self.headers2, callback=self.get_m4a)
            yield Request(item['url'], headers=self.headers2, callback=self.get_m4a)

    def get_m4a(self, respose):
        numlist = respose.xpath('//div[@class="personal_body"]/@sound_ids')[0].split(',')
        for i in numlist:
            murl = 'http://www.ximalaya.com/tracks/{}.json'.format(i)
            html = Request(murl, headers=self.headers1, callback=self.get_m4a)
            dic = json.loads(html)
            item2 = ScrapydetaileItem()
            item2['id'] = dic['id']
            item2['play_path_64'] = dic['play_path_64']
            yield item2


